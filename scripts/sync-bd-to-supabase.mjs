#!/usr/bin/env node
// ── scripts/sync-bd-to-supabase.mjs ────────────────────────────────────────
// Reads `.beads/issues.jsonl` and upserts every issue into the Supabase
// `bd_issues` table, then deletes rows whose ids no longer appear in the
// JSONL.  Result: the table is a faithful mirror of the committed bd
// state at the moment the script ran.
//
// WHY a sync job (instead of reading JSONL at runtime):
//   1. The /roadmap board lives in a static GitHub Pages bundle — no
//      server.  Pulling JSONL into the browser would need a public URL
//      for the file and a polling loop; Supabase Realtime gives us free
//      change-streaming once the row is in Postgres.
//   2. bd's local Dolt database is gitignored, so CI can't read it
//      directly.  The committed JSONL (regenerated by bd's pre-commit
//      hook on every commit) is the canonical source CI can rely on.
//
// RUNNING:
//   Called by `.github/workflows/bd-sync.yml` on every push.  Can also
//   be invoked locally for a one-shot resync:
//     SUPABASE_URL=… SUPABASE_SERVICE_ROLE_KEY=… node scripts/sync-bd-to-supabase.mjs
//
// EXIT CODES:
//   0 — sync completed (or there was nothing to do).
//   1 — required env var missing, JSONL malformed, or Supabase write failed.

import { readFileSync, existsSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { createClient } from '@supabase/supabase-js';

// ── Paths + env ────────────────────────────────────────────────────────────

const HERE   = dirname(fileURLToPath(import.meta.url));
const SOURCE = resolve(HERE, '..', '.beads', 'issues.jsonl');

const SUPABASE_URL              = process.env.SUPABASE_URL;
const SUPABASE_SERVICE_ROLE_KEY = process.env.SUPABASE_SERVICE_ROLE_KEY;

if (!SUPABASE_URL || !SUPABASE_SERVICE_ROLE_KEY) {
  console.error('[bd-sync] missing SUPABASE_URL or SUPABASE_SERVICE_ROLE_KEY env var');
  process.exit(1);
}

// ── Row shape ──────────────────────────────────────────────────────────────
// Mirrors the bd_issues table created in migration 0038 and the
// `BdIssueSchema` in `src/features/roadmap/api/bdIssues.ts`.  Keep all
// three in sync — drift fails loud at the Zod boundary in the UI.

/**
 * Normalise a bd JSONL row to the shape `bd_issues` expects.  Optional
 * fields collapse to null rather than `undefined` so the upsert payload
 * is stable.
 *
 * @param {Record<string, unknown>} row - One parsed JSONL object.
 * @returns {Record<string, unknown>}     Row ready for upsert.
 */
function trim(row) {
  return {
    id:           row.id,
    title:        row.title,
    description:  row.description ?? null,
    notes:        row.notes ?? null,
    status:       row.status,
    priority:     row.priority,
    issue_type:   row.issue_type ?? 'task',
    assignee:     row.assignee ?? null,
    created_at:   row.created_at,
    updated_at:   row.updated_at,
    started_at:   row.started_at ?? null,
    closed_at:    row.closed_at ?? null,
    close_reason: row.close_reason ?? null,
  };
}

// ── Parse JSONL ────────────────────────────────────────────────────────────

/**
 * Read `.beads/issues.jsonl` and return the validated trimmed rows.
 * Blank lines and non-issue records (bd memories) are skipped.  A
 * missing file is treated as "zero issues" — the sync will then delete
 * any existing rows in Supabase, which is the correct behaviour for a
 * repo that has stopped tracking bd.
 */
function readIssues() {
  if (!existsSync(SOURCE)) {
    console.warn(`[bd-sync] ${SOURCE} not found — proceeding with an empty issue set.`);
    return [];
  }
  const raw    = readFileSync(SOURCE, 'utf8');
  const issues = [];

  for (const line of raw.split('\n')) {
    const stripped = line.trim();
    if (!stripped) continue;
    let parsed;
    try {
      parsed = JSON.parse(stripped);
    } catch (err) {
      console.warn('[bd-sync] skipping malformed JSONL line:', err.message);
      continue;
    }
    if (parsed._type !== 'issue') continue;
    if (!parsed.id || !parsed.title || !parsed.status) {
      console.warn('[bd-sync] skipping issue with missing id/title/status:', parsed.id ?? '<unknown>');
      continue;
    }
    issues.push(trim(parsed));
  }
  return issues;
}

// ── Main ───────────────────────────────────────────────────────────────────

async function main() {
  const issues = readIssues();
  const ids    = new Set(issues.map((i) => i.id));

  const client = createClient(SUPABASE_URL, SUPABASE_SERVICE_ROLE_KEY, {
    auth: { persistSession: false },
  });

  // ── Upsert ─────────────────────────────────────────────────────────────
  // One round-trip — Supabase upserts in a single statement.  Conflict
  // target is the primary key.  We don't pass `synced_at`; the default
  // on the column refreshes on every INSERT but not on UPDATE, so we
  // explicitly stamp it client-side instead so updates also bump.
  const now = new Date().toISOString();
  const upsertPayload = issues.map((row) => ({ ...row, synced_at: now }));

  if (upsertPayload.length > 0) {
    const { error: upsertErr } = await client
      .from('bd_issues')
      .upsert(upsertPayload, { onConflict: 'id' });
    if (upsertErr) {
      console.error('[bd-sync] upsert failed:', upsertErr.message);
      process.exit(1);
    }
  }

  // ── Delete tombstones ──────────────────────────────────────────────────
  // Fetch the existing id set and delete anything that no longer appears
  // in the JSONL.  This handles bd issues that were hard-deleted (rare
  // but possible via `bd delete`).  We could `not.in.(…)` directly, but
  // the list-then-delete path keeps the request URL bounded for large
  // tables.

  const { data: current, error: listErr } = await client
    .from('bd_issues')
    .select('id');
  if (listErr) {
    console.error('[bd-sync] list-for-tombstones failed:', listErr.message);
    process.exit(1);
  }

  const tombstones = (current ?? [])
    .map((r) => r.id)
    .filter((id) => !ids.has(id));

  if (tombstones.length > 0) {
    const { error: delErr } = await client
      .from('bd_issues')
      .delete()
      .in('id', tombstones);
    if (delErr) {
      console.error('[bd-sync] delete failed:', delErr.message);
      process.exit(1);
    }
  }

  console.log(
    `[bd-sync] ok — upserted ${upsertPayload.length}, deleted ${tombstones.length}.`,
  );
}

main().catch((err) => {
  console.error('[bd-sync] unexpected error:', err);
  process.exit(1);
});
